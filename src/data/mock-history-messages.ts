/**
 * Mock historical chat messages used to populate the chat window
 * when a user clicks any history item (conversation or delivery record).
 *
 * Each AI assistant message is a JSON string consumed by SmartRenderer.
 * Sources used: "用户直说" / "AI推断" / "默认". We deliberately avoid
 * "待补充" since that triggers SequentialFillIn (interactive UI).
 */
import type { Message } from '../types';

// ===== Helpers =====
let __seq = 1;
function uid(prefix: string): string {
  return `${prefix}-${__seq++}`;
}

function user(content: string, time: string): Message {
  return { id: uid('u'), role: 'user', content, timestamp: time };
}

function ai(time: string, stage: string, sections: unknown[]): Message {
  const payload = JSON.stringify({ stage, sections });
  return { id: uid('a'), role: 'assistant', content: payload, timestamp: time };
}

function aiText(time: string, content: string): Message {
  return { id: uid('a'), role: 'assistant', content, timestamp: time };
}

// ===== Section builders =====
const text = (content: string) => ({ type: 'text', content });
const paramTable = (rows: string[][]) => ({
  type: 'params',
  headers: ['参数', '值', '来源'],
  rows,
});
const dataTable = (headers: string[], rows: string[][]) => ({ type: 'table', headers, rows });
const metrics = (items: Array<{ label: string; value: string }>) => ({ type: 'metrics', items });
const list = (items: string[]) => ({ type: 'list', items });
const confirm = (message: string) => ({ type: 'confirm', message });
const questions = (items: string[]) => ({ type: 'questions', items });


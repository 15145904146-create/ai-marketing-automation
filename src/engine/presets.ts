// ===== Composed full system prompt =====
export function composeSystemPrompt(capability: string | null, scenario: string): string {
  const parts: string[] = [SYSTEM_PERSONA];

  if (capability && CAPABILITY_PRESETS[capability]) {
    parts.push(CAPABILITY_PRESETS[capability]);
  }

  if (SCENARIO_PRESETS[scenario]) {
    parts.push(SCENARIO_PRESETS[scenario]);
  }

  parts.push(RESPONSE_STYLE);
  parts.push(DOMAIN_CONTEXT);

  return parts.join('\n\n---\n\n');
}

